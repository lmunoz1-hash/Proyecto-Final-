    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        int fila = tPersona.getSelectedRow();
        if(fila == -1){
            JOptionPane.showMessageDialog(this,"Seleccione un registro","AVISO", JOptionPane.WARNING_MESSAGE);
        }
        //Se Extraen los valores de la fila o registro que se selecciona
        String id = tPersona.getValueAt(fila, 0).toString();
        String nombre = tPersona.getValueAt(fila, 1).toString();
        String apellido = tPersona.getValueAt(fila, 2).toString();
        int edad = Integer.parseInt(tPersona.getValueAt(fila, 3).toString());
        //Se abre el formulario actualizar con los datos de la fila
        ActualizarPersona actualizar = new ActualizarPersona(id,nombre,apellido,edad);
        actualizar.setVisible(true);
        actualizar.setTitle("ACTUALIZAR PERSONA");
        actualizar.setLocationRelativeTo(null);
        //Cuando la ventana se cierra se recarga la tabla
        actualizar.addWindowListener(new java.awt.event.WindowAdapter() {
            @Override
            public void windowClosed(java.awt.event.WindowEvent e) {
            cargarTabla(); // Refrescar la tabla al cerrar
            }
        });
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        try{
            int fila = tPersona.getSelectedRow();
            if(fila==-1){
                JOptionPane.showMessageDialog(this,"Seleccione un Registro","Advertencia",JOptionPane.WARNING_MESSAGE);
            }
            //Se obtiene el id del registro seleccionado
            String id = tPersona.getValueAt(fila, 0).toString();
            
            int confirmar = JOptionPane.showConfirmDialog(this,"Â¿Desea Eliminar el registro?","Confirmar eliminacion",JOptionPane.YES_NO_OPTION);
            if(confirmar == JOptionPane.YES_OPTION){
                operaciones.eliminarPersona(id);
                cargarTabla();
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,"ERROR al eliminar Persona"+e.getMessage(),"ERROR",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try{
            String id = txtBuscarId.getText().trim();
            //El text box no este vacio
            if(id.isEmpty()){
                JOptionPane.showMessageDialog(this,"Ingrese un Id para buscar");
            }
            // se obtinen los datos de la base de datos
            Persona p = operaciones.obtenerPersona(id);
            // si no se encuentra el id
            if(p == null){
                JOptionPane.showMessageDialog(this,"No existe la Persona");
            }
            //Tabla muestre solo el resultado con el id
            String[] columnas = {"ID","Nombre","Apellido","Edad"};
            DefaultTableModel modelo = new DefaultTableModel(null, columnas);

            Object[] fila = {p.getId(),p.getNombre(),p.getApellido(),p.getEdad()};
            modelo.addRow(fila);

            tPersona.setModel(modelo);
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,"Error al buscar","ERROR",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed
    

    
