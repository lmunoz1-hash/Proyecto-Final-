    public void cargarTabla(){
        //Se crean las columnas de la tabla
        String[] columnas = {"ID","Nombre","Apellido","Edad"};
        DefaultTableModel modelo = new DefaultTableModel(null,columnas){
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // No permitir editar celdas
            }
        };
        tPersona.setModel(modelo);
        //Se obtienen las personas de BD
        List<Persona> lista = operaciones.consultarPersonas();
        //Se llenan las filas de la tabla
        for(Persona p : lista){
            Object[] fila = {p.getId(),p.getNombre(),p.getApellido(),p.getEdad()};
            modelo.addRow(fila);
        }
        
    }

